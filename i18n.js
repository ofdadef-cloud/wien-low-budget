/**
 * Applies translations to all elements with [data-i18n] or [data-i18n-placeholder]
 * @param {string} lang - Optional language code. Fallback to localStorage or 'de'.
 */
function applyI18n(lang) {
  const currentLang = lang || localStorage.getItem('wlb-lang') || 'de';
  const langData = I18N[currentLang] || I18N.de;
  document.documentElement.lang = currentLang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (langData.ui[key]) {
      // Use innerHTML to support formatted text (like <strong> or emojis)
      el.innerHTML = langData.ui[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (langData.ui[key]) {
      el.placeholder = langData.ui[key];
    }
  });
}

/**
 * Initializes a language switcher in the given container
 * @param {string} containerId - ID of the container element
 * @param {function} onChange - Optional callback when language changes
 */
function initLanguageSwitcher(containerId, onChange) {
  const container = document.getElementById(containerId);
  if (!container) return;

  let currentLang = localStorage.getItem('wlb-lang') || 'de';

  // Create button and dropdown
  container.innerHTML = `
    <div class="lang-selector" style="position:relative;">
      <button class="lang-btn" id="global-lang-btn" title="Language" style="background:none; border:1px solid #E8E8E8; cursor:pointer; padding:6px 10px; border-radius:4px; font-size:18px; display:flex; align-items:center; justify-content:center; transition:0.2s;">
        ${I18N[currentLang].flag}
      </button>
      <div class="lang-dropdown" id="global-lang-dropdown" style="display:none; position:absolute; top:calc(100% + 6px); right:0; background:#FFFFFF; border:1px solid #E8E8E8; border-radius:6px; width:180px; box-shadow:0 8px 30px rgba(0,0,0,0.08); z-index:2000; padding:4px 0; max-height:400px; overflow-y:auto;">
        ${Object.keys(I18N).map(code => `
          <div class="lang-item ${code === currentLang ? 'active' : ''}" data-lang="${code}" style="padding:9px 14px; display:flex; align-items:center; gap:10px; cursor:pointer; font-size:13px; transition:0.2s; color:#6B6B6B;">
            <span style="font-size:18px;">${I18N[code].flag}</span>
            <span style="flex:1;">${I18N[code].name}</span>
            ${code === currentLang ? '<span style="color:#E63946;">✓</span>' : ''}
          </div>
        `).join('')}
      </div>
    </div>
  `;

  const btn = container.querySelector('#global-lang-btn');
  const dropdown = container.querySelector('#global-lang-dropdown');

  btn.onclick = (e) => {
    e.stopPropagation();
    dropdown.style.display = dropdown.style.display === 'block' ? 'none' : 'block';
  };

  container.querySelectorAll('.lang-item').forEach(item => {
    item.onclick = () => {
      const newLang = item.dataset.lang;
      localStorage.setItem('wlb-lang', newLang);
      applyI18n(newLang);
      btn.textContent = I18N[newLang].flag;
      
      // Update active state in UI
      container.querySelectorAll('.lang-item').forEach(i => i.classList.remove('active'));
      item.classList.add('active');
      
      dropdown.style.display = 'none';
      if (onChange) onChange(newLang);
    };
  });

  document.addEventListener('click', () => { dropdown.style.display = 'none'; });
  
  // Custom Styles
  if (!document.getElementById('i18n-styles')) {
    const style = document.createElement('style');
    style.id = 'i18n-styles';
    style.textContent = `
      .lang-item:hover { background: #F5F5F3; color: #1A1A1A !important; }
      .lang-item.active { background: rgba(230,57,70,0.06); color: #E63946 !important; }
    `;
    document.head.appendChild(style);
  }
}
